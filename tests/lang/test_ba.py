    def test_string_input(self):
        """Test string input conversion."""
        self.assertEqual(num2words("0", lang="ba"), "нуль")
        self.assertEqual(num2words("1", lang="ba"), "бер")
        self.assertEqual(num2words("10", lang="ba"), "ун")
        self.assertEqual(num2words("100", lang="ba"), "бер йөҙ")
        self.assertEqual(num2words("1000", lang="ba"), "бер мең")
        self.assertEqual(num2words("10000", lang="ba"), "ун мең")
        self.assertEqual(num2words("100000", lang="ba"), "бер йөҙ мең")
        self.assertEqual(num2words("1000000", lang="ba"), "бер миллион")

    def test_edge_cases(self):
        """Test edge cases and special conditions."""
        # Test zero
        self.assertEqual(num2words(0, lang="ba"), "нуль")

        # Test that the converter handles various input types
        self.assertEqual(num2words(100, lang="ba"), num2words("100", lang="ba"))
        self.assertEqual(num2words(1000, lang="ba"), num2words("1000", lang="ba"))

    def test_converter_methods(self):
        """Test direct converter methods for better coverage."""
        from num2words2.lang_BA import Num2Word_BA

        converter = Num2Word_BA()

        # Test direct cardinal conversion
        self.assertIsNotNone(converter.to_cardinal(42))
        self.assertIsNotNone(converter.to_cardinal(1337))

        # Test setup method
        converter.setup()

        # Test negative word if exists
        if hasattr(converter, "negword"):
            self.assertIsNotNone(converter.negword)

        # Test point word if exists
        if hasattr(converter, "pointword"):
            self.assertIsNotNone(converter.pointword)
